#!/bin/bash

while true
do
echo "=============================================================="
echo "		Program Menu :"
echo "		1. Display Free Disk space"
echo "		2. Display Free Memory"
echo "		3. Display Disk Usage"
echo "		4. Network Info"
echo "		5. OS Info"
echo "		0. Exit Program"
echo "=============================================================="

read -p "Choose an option : " option

case $option in 
"1")
	echo "----------------------------------------------------------------------------"
	echo "Displaying Free Disk space"
	df -H
	echo "----------------------------------------------------------------------------"
	;;
"2")
	echo "----------------------------------------------------------------------------"
	echo "Display Free Memory"
	free -m
	echo "----------------------------------------------------------------------------"
	;;
"3")	
	echo "----------------------------------------------------------------------------"
	echo "Display Disk Usage"
	df -a
	echo "----------------------------------------------------------------------------"
	;;
"4")	
	echo "----------------------------------------------------------------------------"
	echo "Network Info"
	ifconfig
	echo "----------------------------------------------------------------------------"
	;;
"5")	
	echo "----------------------------------------------------------------------------"
	echo "OS Info"
	uname -a
	echo "----------------------------------------------------------------------------"
	;;
"0") #try to add "0" or "exit" to quit	
	echo "----------------------------------------------------------------------------"
	echo "Quiting...."
	exit
	;;	
	
*)
	echo "----------------------------------------------------------------------------"
	echo "Invalid Option. Try (0 - 5)"
	echo "----------------------------------------------------------------------------"

esac #end case
done #end while
